						var limitSize = 10485760;
						var fileSize = null;
						try {
							fileSize = parseInt(($(this)[0].files[0].size), 10);
						} catch(e) {}

						if (fileSize != null && fileSize > limitSize) {
							$(this).val('');
							alert("The file size that can be attached is 10MB.");
							return false;
						}
						//파일 용량체크///////////////////////////////////////////

						//파일 확장자 체크///////////////////////////////////////////
						var ext = "PDF,HWP,DOC,DOCX";
						var str = ext.split(",");
						var extName = $(this).val().substring($(this).val().lastIndexOf(".")+1).toUpperCase();
						var ext_chk = false;
						for (var i=0;i<str.length;i++) {
							if(extName == $.trim(str[i])){
								ext_chk = true;
								break;
							} else {
								ext_chk = false;
							}
						}

						if(!ext_chk){
							$(this).val('');
							alert(ext+" You can only upload files.");
							return false;
						}
						//파일 확장자 체크///////////////////////////////////////////

						// 추출한 파일명 삽입
						$(this).siblings('.upload-name').val(filename);
					});

